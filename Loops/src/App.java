public class App {
    public static void main(String[] args){

        // For Loop 
        for(int i=0 ; i<4 ; i++){
            System.out.println("*");
        }

        // While loop
        int j=0;
        while(j<6){
            System.out.println("This is a While loop");
            j++;
        }

        //Do-while loop
        int k=0;
        do{
            System.out.println("This is a Do-while loop");
            k++;
        }while(k<9);

    }
}
