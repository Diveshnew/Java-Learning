public class App {
    public static void main(String[] args){

        // Relational Operator
        int a=10;
        int b=20;
        int c=49;

        System.out.println(a == b);
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= c);
        System.out.println(a != c);

    }
}
